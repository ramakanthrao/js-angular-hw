/**
 * Returns true if the number is prime; otherwise false.
 * See: https://en.wikipedia.org/wiki/Primality_test
 *
 * @param {number} n
 * @return {bool}
 * 
 * @example:
 *   4 => false
 *   5 => true
 *   6 => false
 *   7 => true
 *   11 => true
 *   12 => false
 *   16 => false
 *   17 => true
 */
function isPrime(n) {
    let primeFlag = true;
    for (let index = 2; index <= Math.sqrt(n); index++) {
      if (n % index == 0) {
        primeFlag = false;
        break;
      }
    }
    return primeFlag;
}


module.exports = {
    isPrime: isPrime
};
